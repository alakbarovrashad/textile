import Hero from "@/components/Hero";
import BlogGrid from "@/components/BlogGrid";
import { i18n, Locale } from "@/i18n";
import { supabaseAdmin } from "@/lib/supabase-admin";

interface HomePageProps {
  params: Promise<{ locale: Locale }>;
}

type RawPost = {
  id: string;
  cover_url: string | null;
  category: string | null;
  published: boolean;
  reading_time: number | null;
  created_at: string;
  post_translations: {
    locale: string;
    title: string;
    slug: string;
    excerpt: string | null;
  }[];
};

export function generateStaticParams() {
  return i18n.locales.map((locale) => ({ locale }));
}

export default async function Home({ params }: HomePageProps) {
  const { locale } = await params;

  const { data } = await supabaseAdmin
    .from("about_page")
    .select("*")
    .eq("locale", locale)
    .maybeSingle();

  console.log(data);

  const { data: postsData } = await supabaseAdmin
    .from("posts")
    .select(
      `
      id,
      cover_url,
      category,
      published,
      reading_time,
      created_at,
      post_translations (
        locale,
        title,
        slug,
        excerpt
      )
    `,
    )
    .order("created_at", { ascending: false })
    .limit(6);

  const rawPosts: RawPost[] = (postsData ?? []) as any;

  const blogPosts = rawPosts
    .map((p) => {
      const t =
        p.post_translations.find((x) => x.locale === locale) ||
        p.post_translations[0];

      if (!t) return null;
      const created = new Date(p.created_at);
      const dateLabel = created.toLocaleDateString(
        locale === "tr" ? "tr-TR" : "en-US",
        {
          day: "2-digit",
          month: "short",
          year: "numeric",
        },
      );

      const readingTimeLabel = p.reading_time
        ? `${p.reading_time} ${locale === "tr" ? "DK OKUMA" : "MIN READ"}`
        : "";

      return {
        id: p.id,
        title: t.title,
        excerpt: t.excerpt || "",
        category: p.category || "",
        coverUrl: p.cover_url,
        dateLabel,
        readingTimeLabel,
        slug: t.slug,
      };
    })
    .filter(Boolean) as {
    id: string;
    title: string;
    excerpt: string;
    category: string;
    coverUrl: string | null;
    dateLabel: string;
    readingTimeLabel: string;
    slug: string;
  }[];
  return (
    <>
      <Hero
        content={
          data || {
            hero_title: "Hi! Iâ€™m GÃ¶rkem ðŸ‘‹",
            hero_subtitle:
              "Full Stack Developer | Content Creator | Web Pentester",
            hero_caption: "Lorem ipsum dolor...",
            hero_paragraph1: "Lorem ipsum dolor sit amet...",
            hero_paragraph2: "Aliquam at leo risus...",
            hero_paragraph3: "Praesent vitae eros...",
            hero_cta_label: "Let's work together",
            hero_cta_url: "/contact",
          }
        }
      />
      <BlogGrid locale={locale} posts={blogPosts} />
    </>
  );
}
